        <section>
            <h3>O que é Poliomielite</h3>
            <p> A Poliomielite, também chamada de pólio ou paralisia infantil, é uma doença contagiosa aguda causada
                pelo
                poliovírus, que pode infectar crianças e adultos por meio do contato direto com fezes ou com secreções
                eliminadas
                pela boca das pessoas doentes e provocar ou não paralisia. Nos casos graves, em que acontecem as
                paralisias musculares,
                os membros inferiores são os mais atingidos. </p>

            <p> Embora ocorra com maior frequência em crianças menores de quatro anos, também pode ocorrer em adultos. O
                período de incubação
                da doença varia de dois a trinta dias sendo, em geral, de sete a doze dias.
            </p>

        </section>

        <section>
            <h3>Sintomas</h3>
            <ul>
                <li>Febre;</li>
                <li>Mal-estar;</li>
                <li>Dor de cabeça;</li>
                <li>Dor de garganta e no corpo;</li>
                <li>Vômitos;</li>
                <li>Diarreia;</li>
                <li>Constipação (prisão de ventre);</li>
                <li>Espasmos;</li>
                <li>Rigidez na nuca;</li>
                <li>Instalação súbita de deficiência motora;</li>
                <li>Assimetria acometendo, sobretudo a musculatura dos membros, com mais frequência os inferiores;</li>
                <li>Flacidez muscular, com diminuição ou abolição de reflexos profundos na área paralisada;</li>
                <li>Sensibilidade conservada;</li>
                <li>Persistência de paralisia residual (sequela) após 60 dias do início da doença.</li>
            </ul>
        </section>

        <section>
            <h3>Complicações</h3>
            <ul>
                <li>Problemas e dores nas articulações;</li>
                <li>Pé torto, conhecido como pé equino, em que a pessoa não consegue andar porque o calcanhar não
                    encosta no chão;</li>
                <li>Crescimento diferente das pernas, o que faz com que a pessoa manque e incline-se para um lado,
                    causando escoliose;</li>
                <li>Osteoporose;</li>
                <li>Paralisia de uma das pernas;</li>
                <li>Paralisia dos músculos da fala e da deglutição, o que provoca acúmulo de secreções na boca e na
                    garganta;</li>
                <li>Dificuldade de falar;</li>
                <li>Atrofia muscular;</li>
                <li>Hipersensibilidade ao toque.</li>
            </ul>
        </section>


        <section>
            <h3>Como é feito o diagnóstico?</h3>
            <p> O diagnóstico da poliomielite deve ser suspeitado sempre que houver paralisia flácida de surgimento
                agudo
                com diminuição ou abolição de reflexos tendinosos em menores de 15 anos. Os exames de liquor (cultura) e
                a
                eletromiografia são recursos diagnósticos importantes. O diagnóstico será
                dado pela detecção de poliovírus nas fezes</p>
        </section>


        <section>
            <h3>Transmissão</h3>

            <p> A Uma pessoa pode transmitir diretamente para a outra. A transmissão do vírus da poliomielite se dá
                através
                da boca, com material contaminado com fezes (contato fecal-oral), o que é crítico quando as condições
                sanitárias
                e de higiene são inadequadas. Crianças mais novas, que ainda não adquiriram completamente hábitos de
                higiene,
                correm maior risco de contrair a doença.
            </p>

            <p>
                O Poliovírus também pode ser disseminado por contaminação da água e de alimentos por fezes. A doença
                também pode
                ser transmitida pela forma oral-oral, através de gotículas expelidas ao falar, tossir ou espirrar. O
                vírus se multiplica,
                inicialmente, nos locais por onde ele entra no organismo (boca, garganta e intestinos). Em seguida, vai
                para a corrente
                sanguínea e pode chegar até o sistema nervoso, dependendo da pessoa infectada. Desenvolvendo ou não
                sintomas, o indivíduo
                infectado elimina o vírus nas fezes, que pode ser adquirido por outras pessoas por via oral. A
                transmissão ocorre com mais
                frequência a partir de indivíduos sem sintomas.

            </p>
        </section>


        <section>
            <h3>Prevenção</h3>
            <p> A poliomielite não tem tratamento específico. A doença deve ser evitada tanto através da vacinação
                contra poliomielite como de
                medidas preventivas contra doenças transmitidas por contaminação fecal de água e alimentos. As más
                condições habitacionais, a higiene
                pessoal precária e o elevado número de crianças numa mesma habitação também são fatores que favorecem a
                transmissão da poliomielite.

                Logo, programas de saneamento básico são essenciais para a prevenção da doença. No Brasil, a vacina é
                dada rotineiramente nos postos da
                rede municipal de saúde e durante as campanhas nacionais de vacinação. A vacina contra a poliomielite
                oral trivalente deve ser administrada
                aos dois, quatro e seis meses de vida. O primeiro reforço é feito aos 15 meses e o outro entre quatro e
                seis anos de idade. Também é necessário
                vacinar-se em todas as campanhas.
            </p>
        </section>

