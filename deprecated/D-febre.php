            <h3>Sintomas</h3>
            <ul>
                <li>Início súbito de febre;</li>
                <li>Calafrios;</li>
                <li>Dor de cabeça intensa;</li>
                <li>Dores nas costas;</li>
                <li>Dores no corpo em geral;</li>
                <li>Náuseas e vômitos;</li>
                <li>Fadiga e fraqueza</li>
            </ul>
        </section>

        <section>
            <h3>Complicações</h3>
            <p>Em casos graves, a pessoa infectada por febre amarela pode desenvolver algumas complicações, como:</p>
            <ul>
                <li>Febre alta;</li>
                <li>Icterícia (coloração amarelada da pele e do branco dos olhos);</li>
                <li>Hemorragia (especialmente a partir do trato gastrointestinal);</li>
                <li>Choque e insuficiência de múltiplos órgãos.</li>
            </ul>
        </section>

        <section>
            <h3>Diagnóstico</h3>
            <p> Somente um médico é capaz de diagnosticar e tratar corretamente a febre amarela.
                No caso de qualquer um dos sintomas da doença, procure imediatamente uma unidade de saúde para avaliação
                médica adequada.
                O profissional fará os exames necessários para diagnosticar a doença, assim como sua gravidade, para
                escolher a melhor forma de tratamento.</p>
        </section>

        <section>
            <h3>Transmissão</h3>
            <p> O vírus da febre amarela é transmitido pela picada dos mosquitos transmissores infectados. A doença não
                é passada de pessoa a pessoa. A série
                histórica da doença no Brasil tem demonstrado maior frequência de ocorrência de casos humanos nos meses
                de dezembro e maio, como um padrão
                sazonal. Esse fato ocorre principalmente no verão, quando a temperatura média aumenta na estação das
                chuvas, favorecendo a reprodução e proliferação
                de mosquitos (vetores) e, por consequência o potencial de circulação do vírus.
            </p>
            <p>
                Os vetores silvestres têm hábito diurno, realizando o repasto sanguíneo durante as horas mais quentes do
                dia, sendo os vetores dos gêneros Haemagogus
                e Sabethes, geralmente, mais ativos entre às 9h e 16h da tarde. Há dois diferentes ciclos
                epidemiológicos de transmissão:
                <ul>
                    <li>Silvestre;</li>
                    <li>Urbano.</li>
                </ul>
            </p>
            <p>
                Apesar desses ciclos diferentes, a febre amarela tem as mesmas características sob o ponto de vista
                etiológico, clínico, imunológico e fisiopatológico.
                No ciclo silvestre da febre amarela, os primatas não humanos (macacos) são os principais hospedeiros e
                amplificadores do vírus e os vetores são mosquitos
                com hábitos estritamente silvestres, sendo os gêneros Haemagogus e Sabethes os mais importantes na
                América Latina. Nesse ciclo, o homem participa como um
                hospedeiro acidental ao adentrar áreas de mata. No ciclo urbano, o homem é o único hospedeiro com
                importância epidemiológica e a transmissão ocorre a partir de vetores urbanos (Aedes aegypti)
                infectados.
            </p>
        </section>

        <section>
            <h3>Prevenção</h3>
            <p> A vacina é a principal ferramenta de prevenção e controle da febre amarela. O Sistema Único de Saúde
                (SUS) oferta vacina contra febre amarela para a população. Desde abril de 2017, o Brasil adota o esquema
                vacinal de apenas uma dose durante toda a vida, medida que está de acordo com as recomendações da
                Organização Mundial de Saúde (OMS). Toda pessoa que reside em Áreas com Recomendação da Vacina contra
                febre
                amarela e pessoas que vão viajar para essas áreas deve se imunizar. A vacina, que é administrada via
                subcutânea, está disponível durante todo o ano nas unidades de saúde e deve ser administrada pelo menos
                10
                dias antes do deslocamento para áreas de risco, principalmente, para os indivíduos que são vacinados
                pela primeira vez.
            </p>
